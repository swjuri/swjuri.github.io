$("img").hide();

$(".h1Btn").hover(
	function() {
		$(".h1")
			.find("img")
			.show();
	},
	function() {
		$(".h1")
			.find("img")
			.hide();
	}
);

$(".h1Btn").mousemove(function(e) {
	$(".h1")
		.css("left", e.pageX + 20)
		.css("top", e.pageY + 20);
});

